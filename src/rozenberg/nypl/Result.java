package rozenberg.nypl;

public class Result {
	private String apiItemURL;
	private String title;

	public String getApiItemURL() {
		return apiItemURL;
	}

	public void setApiItemURL(String apiItemURL) {
		this.apiItemURL = apiItemURL;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String toString() {
		return title;
	}

}
